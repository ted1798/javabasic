package ch02;

public class Ex01 {

	public static void main(String[] args) {
		System.out.println(10+5);
		System.out.println(10-5);
		System.out.println(10*5);
		System.out.println(10/5);

		//변수(메모리공간)를 사용했을 때 데이터 수정이 쉬움. 
		int x = 10;
		int y = 5;
		
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		
	}

}
