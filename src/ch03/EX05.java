package ch03;

public class EX05 {

	public static void main(String[] args) {
		// 논리 연산자
		// 종류 : &&,||,!, ^(두개가 달라야 true)
		
		System.out.println(true && false);
		System.out.println(true && true);
		System.out.println(true || false || false || true);
		
		
		System.out.println(true ^ true);
		System.out.println(true ^ false);
		System.out.println(false ^ false);
		
		

	}

}
