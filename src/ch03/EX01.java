package ch03;

public class EX01 {

	public static void main(String[] args) {
		// 연산자란
		// 종류 : 산술, 단항, 비교, 논리, 쉬프트, 기타 연산자
		
		// 산술연산자
		int a = 10; //4byte
		int b = 3;
		double result = (double)(a/b);
		//((double)a/b)
		System.out.println(a+b);
		System.out.println(result);
		System.out.println(a%b);
		

	}

}
